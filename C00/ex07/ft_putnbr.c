#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	x;

	if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		nb = nb - nb * 2;
		write(1, "-", 1);
	}
	x = nb + '0';
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		write(1, &x, 1);
}
