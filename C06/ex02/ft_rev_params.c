#include <unistd.h>

void	ft_putchar(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	int	i;

	i = argc - 1;
	while (argv[i] > argv[0])
	{
		ft_putchar(argv[i]);
		write(1, "\n", 1);
		i--;
	}
}
